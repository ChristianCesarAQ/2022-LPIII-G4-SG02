    private void jTextFieldNDocumentoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNDocumentoKeyTyped
        char validar = evt.getKeyChar();

        if (Character.isLetter(validar)) {
            getToolkit().beep();
            evt.consume();

            JOptionPane.showMessageDialog(null, "Solo números", "¡Advertencia!", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jTextFieldNDocumentoKeyTyped

    private void jTextFieldNombresKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNombresKeyTyped
        char validar = evt.getKeyChar();

        if (Character.isDigit(validar)) {
            getToolkit().beep();
            evt.consume();

            JOptionPane.showMessageDialog(null, "Solo letras", "¡Advertencia!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jTextFieldNombresKeyTyped

