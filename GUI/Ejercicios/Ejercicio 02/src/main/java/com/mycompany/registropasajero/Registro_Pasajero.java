    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed

        if (jTextFieldNombres.getText().equals("")
    
                || jComboBoxTipoDocumento.getSelectedItem().equals("Seleccionar")
                || jTextFieldNDocumento.getText().equals("")
                || buttonGroupSexo.getSelection().equals(false)                
                || jTextField1.getText().equals("")
                || jComboBoxOrigen.getSelectedItem().equals("")
                || jComboBoxDestino.getSelectedItem().equals("")
                || jListCalidadServicio.getSelectedValue().equals("")
                || buttonGroupPiso.getSelection().equals(false))   {
            JOptionPane.showMessageDialog(null, "Complete todos los datos", "¡Advertencia!", JOptionPane.WARNING_MESSAGE);
        } else {
            if (jCheckBoxAudifonos.isSelected()) {
                items = items + "\n- Audifonos";
            }

            if (jCheckBoxManta.isSelected()) {
                items = items + "\n- Manta";
            }

            if (jCheckBoxRevistas.isSelected()) {
                items = items + "\n- Revistas";
            }

            JOptionPane.showMessageDialog(null, "\nNombres: " + jTextFieldNombres.getText().toUpperCase()

                    + "\nTipo doc.: " + jComboBoxTipoDocumento.getSelectedItem() +": "+ jTextFieldNDocumento.getText()
                    + "\nGénero: " + jRadioButtonMasculino.getText()
                    + "\nFecha de viaje: " + jTextField1.getText()
                    + "\nOrigen: " + jComboBoxOrigen.getSelectedItem()
                    + "\nDestino: " + jComboBoxDestino.getSelectedItem()
                    + "\nCalidad de servicio: " + jListCalidadServicio.getSelectedValue()
                    + "\nNumeri de piso: " + jRadioButtonPiso1.getText()
                    + "\nServicio adicional: " + items, "Datos registrados del pasaje: ", WIDTH, null);

            dispose();
            new Registro_Pasajero().setVisible(true);
        }
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

